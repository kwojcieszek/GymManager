        private static void CabinetKey(int quantity)
        {
            var db = new GymManagerContext();

            var users = (from m in db.Users
                select new { m.UserID }).ToList();

            for(var i = 0; i < quantity; i++)
            {
                var cabinetKey = new CabinetKey
                {
                    Name = $"0{i}",
                    AddedBy = users[RandomM1(users.Count)].UserID,
                    IsAcive = true,
                    DateAdded = DateTime.Now
                };

                db.CabinetKeys.Add(cabinetKey);
            }

            db.SaveChanges();
        }

        private static void EntryRegistry(int quantity)
        {
            var db = new GymManagerContext();

            var members = (from m in db.Members
                select new { m.MemberID }).ToList();

            var cabinetKeys = (from m in db.CabinetKeys
                select new { m.CabinetKeyID }).ToList();

            for(var i = 0; i < quantity; i++)
            {
                var entryRegistry = new EntryRegistry
                {
                    MemberID = members[RandomM1(members.Count)].MemberID,
                    EntryDate = DateTime.Now.AddDays(-Random(0, 300)),
                    CabinetKeyID = cabinetKeys[RandomM1(cabinetKeys.Count)].CabinetKeyID
                };

                if(entryRegistry.EntryDate.AddHours(3) > DateTime.Now)
                {
                    entryRegistry.IsAcive = true;
                }
                else
                {
                    entryRegistry.ExitDate = entryRegistry.EntryDate.AddHours(Random(1, 4))
                        .AddMinutes((int)(RandomDecimal() * 10));
                    entryRegistry.VisitTime =
                        (int)(entryRegistry.ExitDate.Value - entryRegistry.EntryDate).TotalMinutes;
                    entryRegistry.IsAcive = false;
                }


                db.EntriesRegistry.Add(entryRegistry);
            }

            db.SaveChanges();
        }

        private void Members(int quantity)
        {
            var db = new GymManagerContext();

            var users = (from m in db.Users
                select new { m.UserID }).ToList();

            for(var i = 0; i < quantity; i++)
            {
                var member = new Member
                {
                    Id = $"00{i}",
                    FirstName = _firstNames[RandomM1(_firstNames.Count)].FirstCharToUpper(),
                    LastName = _lastNames[RandomM1(_lastNames.Count)].FirstCharToUpper(),
                    City = _cities[RandomM1(_cities.Count)],
                    Street1 = _streets[RandomM1(_streets.Count)] + $" {RandomM1(20)}/{RandomM1(60)}",
                    Street2 = string.Empty,
                    PostCode = $"{Random(1, 30)}-{Random(100, 800)}",
                    Email = $"{_firstNames[RandomM1(_firstNames.Count)].ToLower()}@op.pl",
                    Phone = $"{Random(600000000, 800000000)}",
                    PassID = Random(1, 5),
                    AddedBy = users[RandomM1(users.Count)].UserID,
                    IsAcive = true,
                    DateAdded = DateTime.Now
                };

                db.Members.Add(member);
            }

            db.SaveChanges();
        }

        private static int Random(int min, int max)
        {
            return new Random().Next(min, max);
        }

        private static decimal RandomDecimal()
        {
            return (decimal)new Random().NextDouble();
        }

        private static int RandomM1(int max)
        {
            return new Random().Next(max - 1);
        }

        private void Users(int quantity)
        {
            var db = new GymManagerContext();

            for(var i = 0; i < quantity; i++)
            {
                var user = new User
                {
                    FirstName = _firstNames[RandomM1(_firstNames.Count)].FirstCharToUpper(),
                    LastName = _lastNames[RandomM1(_lastNames.Count)].FirstCharToUpper()
                };
                user.UserName = $"{user.FirstName[..1].ToLower()}{user.LastName.ToLower()}";
                user.Email = $"{_firstNames[RandomM1(_firstNames.Count)].ToLower()}@op.pl";
                user.IsAcive = true;
                user.IsAdmin = false;
                user.DateAdded = DateTime.Now;
                user.Phone = $"{Random(600000000, 800000000)}";
                user.Password = Cryptography.MD5Hash("user.UserName");

                db.Users.Add(user);
            }

            db.SaveChanges();
        }
    }
